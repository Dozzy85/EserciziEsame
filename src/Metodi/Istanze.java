package Metodi;

public class Istanze {

	private int num;
	private int area;
	private String servizi;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getArea() {
		return area;
	}
	public void setArea(int area) {
		this.area = area;
	}
	public String getServizi() {
		return servizi;
	}
	public void setServizi(String servizi) {
		this.servizi = servizi;
	}
	
	
	
}
